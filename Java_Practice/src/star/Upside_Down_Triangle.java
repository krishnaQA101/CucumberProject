package star;

public class Upside_Down_Triangle {

	public static void main(String[] args) {
		
	  for(int i=100;i>0;i--) {
		  
		  for(int k=0;k<100-i;k++) {
			  
			  System.out.print(" ");
		  }
		  
		  for(int j=0;j<i;j++) {
			  
			  System.out.print("* ");
			  
			  
		  }
		  
		  System.out.println();
		  
		  
		  
		  
	  }
		
		
		
	}

}
